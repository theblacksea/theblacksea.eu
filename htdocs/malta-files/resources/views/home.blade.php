                    <h2>The Malta Files: Avrupa Birliği’nin en küçük ülkesi nasıl vergi cennetine dönüştü</h2>
                    <p>Akdeniz adası Malta, AB ülkeleri içinde şirketlerin kazancı üzerinden en az vergi isteyen ülke – vergi oranı sadece yüzde beş.</p>
                    <p>Geçtiğimiz üç ay boyunca, European Investigative Collaborations (EIC) sayısı 150 bini geçen belgeyi inceledi ve uluslararası şirketlerin bu sistemden nasıl faydalandıklarını ve Malta’yı vergi kaçırmak için nasıl kullandıklarını ortaya çıkardı.</p>
                    <p>AB üyesi olmanın avantajlarını da kullanan Malta, aynı zamanda kendi ülkelerinde vergi ödemek istemeyen büyük şirketleri ve varlıklı yabancıları da kendine çekmeye çalışıyor.</p>
                    <p>Bu durum Malta’yı İtalyan mafyası, karanlık Rus işadamları ve tanınmış Türkler’in de favori iş merkezlerinden biri haline getirdi.</p>
                    <p>Malta üzerinden vergi kaçırılması AB’nin bütçesini etkilerken aynı zamanda Avrupa’nın vergilendirme sistemindeki zayıflığı da ortaya çıkarmış oluyor.</p>
                    <p>Bu araştırma, EIC gazetecilik ağı içinde bulunan 16 ülkeden 12 medya kuruluşunu ve 38 gazetecinin bir araya gelmesiyle hayata geçti.</p>
                    <p>Düzen şöyle işliyor:</p>
                    <p>İstanbul, Londra ya da Paris’teki bir şirket, kurumlar vergisinin yüzde 35 olduğu Malta’da yan bir şirket açıyor. Aslında yüzde 35, AB’deki en yüksek vergi oranlarından.</p>
                    <p>Ancak, eğer şirket sahipleri Malta’da yaşamıyorsa ve şirketin işlerinin büyük bir kısmı Malta üzerinden yürümüyorsa, o zaman Malta Gelir İdaresi bu kesilen verginin 6/7’sini şirkete geri iade ediyor.</p>
                    <p>Böylece, pratikte Malta’daki vergi oranı sadece yüzde 5’e inmiş oluyor.</p>
                    <p>Malta Today gazetesine göre, 2015 yılında bu vergi düzeni Malta’ya dört milyar avro kaybettirdi. Her geçtiğimiz gün de bu rakam artıyor.</p>
                    <p>Bu “kayıp” para hem Malta’yı hem de şirketlerin asıl kurulu oldukları ve iş yaptıkları ülkeleri zarara sokmuş oluyor.</p>
                    <p>Durum kanuna aykırı değil ancak Malta vergi cenneti olmanın getirdiği kara para ve yolsuzluk gibi sorunları araştırma konusunda sınıfta kalıyor.</p>
                    <p>Malta 2014 yılında AB’ye katıldı ve şu anda AB dönem başkanlığını yürütüyor. Tam da AB gündeminde vergi kaçırma ve kara parayla mücadelenin başı çektiği zamanda.</p>
                    <p>Malta Ekonomi Bakanı Edward Scicluna’ya göre, konumundan ve doğal kaynaklarının azlığından dolayı ülkenin uluslararası şirketler için cazibe merkezi haline gelmesi şart.</p>
                    <p>Malta, 450 bin nüfusuyla AB’nin en küçük ülkesi. Ülkenin politikacıları bu küçük adanın diğer Avrupa ülkeleriyle rekabet edebilmesi için uygun vergi koşulları olması gerektiğini savunuyorlar.</p>
                    <p>The Black Sea, sızdırılan belgeleri ve Malta ticaret sicil kayıtlarını inceleyerek şirketlerin bu finansal düzeni vergi kaçırmak için nasıl kullandığına ışık tutuyor.</p>
