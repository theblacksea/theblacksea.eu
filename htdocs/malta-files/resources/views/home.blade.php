                    <h1>The Malta Files: How the smallest EU country became a haven for global tax avoidance</h1>
                    <p>
                        The Mediterranean republic of Malta operates a tax system where companies pay the lowest tax on profits in the EU - only five per cent.
                    </p>
                    <p>
                        Over the last three months, journalistic network European Investigative Collaborations (EIC) dug into over 150,000 documents that show how international companies take advantage of this system, using Malta as a pirate base for tax avoidance in the EU.
                    </p>
                    <p>
                        Although benefiting from the advantages of EU membership, Malta also welcomes large companies and wealthy private clients looking to dodge taxes in their home countries.
                    </p>
                    <p>
                        This has made Malta a target for firms linked to the Italian mafia, Russian loan sharks and the highest echelons of the Turkish elite.
                    </p>
                    <p>
                        This damages the budgets of other EU countries, and reveals a weakness in the union, which allows member states sovereign rights over their taxation.
                    </p>
                    <p>
                        The research was undertaken by the EIC, which has brought together 12 media and over 40 journalists in 16 countries.
                    </p>
                    <p>
                        This is how the scheme works:
                    </p>
                    <p>
                        A company in Geneva, London or Paris can open a parent firm in Malta, where it is taxed at 35 per cent, the highest income tax band.
                    </p>
                    <p>
                        However if the shareholders of the company are not based in Malta, and the bulk of the firm’s business does not take place in Malta, the Maltese Inland Revenue can refund up to 6/7 of this amount to the company.
                    </p>
                    <p>
                        De facto, this makes corporate tax in Malta only five per cent.
                    </p>
                    <p>
                        This compares to an EU average of around 22 per cent.
                    </p>
                    <p>
                        In 2015, this scheme saw a shortfall of almost four billion Euro in taxes, according to a study by newspaper Malta Today. This figure has been steadily rising year on year. 
                    </p>
                    <p>
                        This is money “lost” to both the Maltese exchequer and to the tax base of European countries where the companies are headquartered.
                    </p>
                    <p>
                        Even if this situation appears legal, it seems Malta has not met all the requirements to control possible fraud cases associated with the fiscal status of the islands state. 
                    </p>
                    <p>
                        Malta joined the EU in 2004 and holds the EU Presidency between January and June 2017 - during a climate where the fight against tax evasion, tax avoidance and money laundering is top of the EU agenda.
                    </p>
                    <p>
                        Maltese Finance Minister Edward Scicluna says that due to Malta’s location and lack of natural resources, the country must be attractive for international companies.
                    </p>
                    <p>
                        Malta itself is the smallest country in the EU, with only 450,000 people. Its leaders have complained that controlling tax policy is the only tool left for small EU countries to remain competitive. 
                    </p>
                    <p>
                        Using leaked documents and the Maltese company registry, The Black Sea reveals how companies have planned and operated these schemes to deprive countries across the world of valuable revenues.
                    </p>
